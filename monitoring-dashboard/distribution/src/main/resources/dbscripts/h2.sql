CREATE TABLE IF NOT EXISTS HEARTBEAT(
    GROUP_ID VARCHAR (255),
    NODE_ID VARCHAR (255),
    HERTBEAT_INTERVAL INTEGER,
    MGT_API_URL VARCHAR (255),
    TIMESTAMP VARCHAR (20),
    ACCESS_TOKEN VARCHAR (800),
    PRIMARY KEY (GROUP_ID, NODE_ID)
);

